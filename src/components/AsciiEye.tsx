export default function AsciiEye({ bars }: { bars: number[] }) {
  // Convert bars (0–100) to a small ASCII “eye bars”
  const lines = bars.map((h) => {
    const level = Math.round((h / 100) * 3);
    return "▁▂▃▄▅▆▇█".charAt(level);
  });
  return (
    <pre className="text-[#6fb6c4] font-mono text-[10px] leading-[8px] select-none">
      {lines.join("")}
    </pre>
  );
}
